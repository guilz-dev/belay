import { writeFile } from 'node:fs/promises'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..')
const outFile = path.join(root, 'src/corpus/standing-allow-catalog.generated.ts')

const { loadCorpusCases, DEFAULT_CORPUS_REPO_ROOT, runCorpusEvaluation, passesHardGates } = await import(
  path.join(root, 'dist/corpus/evaluate.js')
)
const { MUST_ALLOW_SHELL_COMMANDS } = await import(
  path.join(root, 'dist/corpus/must-allow-commands.js')
)
const { classifyShell } = await import(path.join(root, 'dist/core/verdict/adapter.js'))
const { DEFAULT_CONFIG_V3, classifierOptionsFromConfig } = await import(
  path.join(root, 'dist/core/config.js')
)
const { createDeterministicJudgeStub } = await import(path.join(root, 'dist/core/verdict/judge.js'))

const corpusDir = path.join(root, 'corpus')
const corpusMetrics = await runCorpusEvaluation(corpusDir)
if (!passesHardGates(corpusMetrics.gates)) {
  throw new Error(
    `Refusing to regenerate standing-allow catalog while corpus hard gates fail (must-ask misses=${corpusMetrics.gates.mustAsk.mismatches}, provably-benign blocks=${corpusMetrics.gates.provablyBenign.mismatches}). Run pnpm corpus and fix the corpus first.`,
  )
}
const cases = await loadCorpusCases(corpusDir)
const repoRoot = DEFAULT_CORPUS_REPO_ROOT
const cwd = path.join(repoRoot, 'src')
const options = classifierOptionsFromConfig(DEFAULT_CONFIG_V3)
const judge = createDeterministicJudgeStub()

async function shellCatalogEntry(command) {
  const result = await classifyShell(command, cwd, repoRoot, DEFAULT_CONFIG_V3, options, judge)
  return {
    command,
    normalizedCommand: result.normalizedCommand ?? command,
    runtimeKey: result.fingerprint,
  }
}

const provablyBenign = await Promise.all(
  cases
    .filter((entry) => entry.category === 'provably-benign' && entry.kind === 'shell')
    .map((entry) => shellCatalogEntry(entry.command)),
)

const mustAllow = await Promise.all(
  MUST_ALLOW_SHELL_COMMANDS.map((command) => shellCatalogEntry(command)),
)

const catalog = {
  version: 1,
  shell: {
    provablyBenign,
    mustAllow,
  },
}

const source = `// Generated by scripts/generate-standing-allow-catalog.mjs — do not edit.
export interface StandingAllowCatalogShellEntry {
  command: string
  normalizedCommand: string
  runtimeKey: string
}

export interface StandingAllowCatalog {
  version: 1
  shell: {
    provablyBenign: StandingAllowCatalogShellEntry[]
    mustAllow: StandingAllowCatalogShellEntry[]
  }
}

export const STANDING_ALLOW_CATALOG: StandingAllowCatalog = ${JSON.stringify(catalog, null, 2)}
`

await writeFile(outFile, source, 'utf8')
const { spawnSync } = await import('node:child_process')
spawnSync('pnpm', ['exec', 'biome', 'check', '--write', outFile], {
  cwd: root,
  stdio: 'inherit',
})
console.log(`Wrote ${outFile}`)
console.log(
  `  provably-benign: ${provablyBenign.length}, must-allow: ${mustAllow.length} shell entries`,
)
